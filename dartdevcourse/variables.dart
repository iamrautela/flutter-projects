void main() {
  //Variables

  //first value:
  print(19);

  //second value
  print(5);

  print(19 * 5);
  print(19 + 5);
  print(19 - 5);

  //another way:
  print(10);
  print(12);
  print(10 * 12);
  print(10 - 12);

  // <datatype> <variablename> = value;

  //INTEGER DATA TYPE:
  int first = 310;
  int second = 310;

  print(first + second);
  print(first * second);

  //DOUBLE DATA TYPE:
  double f = 310.22;
  double s = 310.22;

  print(f + s);
  print(f * s);

  //STRING DATATYPE:
  String strvalue = 'Hello';

  print(strvalue);
}
